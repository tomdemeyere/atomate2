def test_get_cell_neighbors(
    b4_h12_structure: Structure, c2_h12_b10_molecule: Molecule
) -> None:
    connectivity = get_connectivity_matrix(
        b4_h12_structure, cutoff_multiplier=np.sqrt(2)
    )

    multi_center_bonds = get_three_center_bonds(
        connectivity_matrix=connectivity,
        unique_sites=list(range(len(b4_h12_structure))),
    )

    cell_neighbors = get_cell_neighbors(b4_h12_structure, multi_center_bonds)

    assert_array_equal(cell_neighbors[0], np.zeros((3, 3)))
    assert_array_equal(cell_neighbors[-1], [[0, 0, 0], [-1, 0, -1], [0, -1, 0]])

    c2_h12_b10_structure = c2_h12_b10_molecule.get_boxed_structure(
        a=15, b=15, c=15, offset=7.5
    )

    connectivity = get_connectivity_matrix(
        c2_h12_b10_structure, cutoff_multiplier=np.sqrt(2)
    )

    multi_center_bonds = get_three_center_bonds(
        connectivity_matrix=connectivity,
        unique_sites=list(range(len(c2_h12_b10_structure))),
    )

    cell_neighbors = get_cell_neighbors(c2_h12_b10_structure, multi_center_bonds)

    assert_array_equal(cell_neighbors, np.zeros((len(multi_center_bonds), 3, 3)))
